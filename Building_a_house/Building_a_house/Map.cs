        public void PrintMap()
        {
            Console.SetCursorPosition(0, 0);
            for (int i = 0; i < MapTile.GetLength(0); i++)
            {
                for (int j = 0; j < MapTile.GetLength(1); j++)
                {
                    if (MapTile[i, j] == false)
                    {
                        Console.Write("■");
                    }
                    else
                    {
                        Console.Write("  ");
                    }
                }
                Console.WriteLine();
            }
            foreach (var stone in stones.Where(s => !s.IsCollected))
            {
                stone.PrintStone();
            }
        }
        public Stone GetStone(int y, int x)
        {
            return stones.FirstOrDefault(s => s.Position.Y == y && s.Position.X == x);
        }
        public void RemoveStone(int y, int x)
        {
            Stone stone = stones.FirstOrDefault(s => s.Position.Y == y && s.Position.X == x);
            if (stone != null)
            {
                stones.Remove(stone);
            }
        }
        public void RandomStones(Inventory _inventory)
        {
            if (!_inventory.IsFull())
            {
                int generatinglimits = stones.Count(s => !s.IsInstalled);

                if (generatinglimits >= 10)
                {
                    return;
                }
                var duplication = new List<Point>();
                for (int i = 0; i < MapTile.GetLength(0); i++)
                {
                    for (int j = 0; j < MapTile.GetLength(1); j++)
                    {
                        if (MapTile[i, j] && stones.All(s => s.Position.Y != i || s.Position.X != j))
                        {
                            duplication.Add(new Point(i, j));
                        }
                    }
                }

                if (duplication.Count > 0)
                {
                    var randomPosition = duplication[new Random().Next(duplication.Count)];
                    stones.Add(new Stone(randomPosition.Y, randomPosition.X));
                }
            }
        }

        public void PlaceStone(int y, int x, Stone stone)
        {
            if (MapTile[y, x])
            {
                stones.Add(stone);
            }
